public class Body {
    private String hair;
    private String rost;
    public Body(String hair, String rost) {
        this.hair = hair;
        this.rost = rost;
    }

    public void setHair(String hair) {
        this.hair = hair;
    }

    public void setRost(String rost) {
        this.rost = rost;
    }



    public String getHair() {
        return hair;
    }

    public String getRost() {
        return rost;
    }
}
